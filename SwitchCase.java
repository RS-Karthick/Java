
public class SwitchCase {
	public static void main(String[] args) {
		char result = '3';
		switch(result) 
		{
		case 'f' :
			System.out.println("You are the Fail \n  try again \n     all the best");
			break;
		case 'p' :
			System.out.println("You are the Pass \n Congratulations!!!");
		default :
			System.out.println("The will be shown soon \n  kindly wait");	
		}
	}
}

